package com.example.demo3A;

public class Forecasting {

        private double revenue;
        private double cogs;
        private double fixedCosts;
        private double variableCosts;
        private double taxRate;

        // Getters and setters
        public double getRevenue() { return revenue; }
        public void setRevenue(double revenue) { this.revenue = revenue; }

        public double getCogs() { return cogs; }
        public void setCogs(double cogs) { this.cogs = cogs; }

        public double getFixedCosts() { return fixedCosts; }
        public void setFixedCosts(double fixedCosts) { this.fixedCosts = fixedCosts; }

        public double getVariableCosts() { return variableCosts; }
        public void setVariableCosts(double variableCosts) { this.variableCosts = variableCosts; }

        public double getTaxRate() { return taxRate; }
        public void setTaxRate(double taxRate) { this.taxRate = taxRate; }
    }


